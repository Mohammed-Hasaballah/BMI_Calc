                                fontWeight: FontWeight.bold,
                              ),
                            ),
                          ],
                        ),
                        decoration: BoxDecoration(
                          borderRadius: BorderRadius.circular(10.0),
                          color: isMale
                              ? const Color(0xff333244)
                              : const Color.fromARGB(255, 76, 75, 102),
                        ),
